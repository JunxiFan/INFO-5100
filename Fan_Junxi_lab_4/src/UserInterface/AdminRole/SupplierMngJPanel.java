    private void viewSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSupplierBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = CatalogTbl.getSelectedRow();
        if(selectedRow<0)
             JOptionPane.showMessageDialog(null, "Please select any row", "warning", JOptionPane.WARNING_MESSAGE);
        else{
            Supplier supplier = (Supplier) CatalogTbl.getValueAt(selectedRow, 0);
            ViewSupplierJPanel panel = new ViewSupplierJPanel(userProcessContainer, supplier);
            userProcessContainer.add("ViewSupplierJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_viewSupplierBtnActionPerformed

    private void deleteSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSupplierBtnActionPerformed
        // TODO add your handling code here:
        int selectedrow = CatalogTbl.getSelectedRow();
        
        if(selectedrow >=0)
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete?", "warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Supplier supplier = (Supplier) CatalogTbl.getValueAt(selectedrow, 0);
                supplierDirectory.deleteSupplier(supplier);
                JOptionPane.showMessageDialog(null, "Supplier has been deleted");
                populateTable();
            }

            
        }
        else
            JOptionPane.showMessageDialog(null, "Please select any row", "warning", JOptionPane.WARNING_MESSAGE);
                                           
    }//GEN-LAST:event_deleteSupplierBtnActionPerformed


