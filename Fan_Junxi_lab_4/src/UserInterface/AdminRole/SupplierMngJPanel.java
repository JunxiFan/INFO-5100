    void populateTable() {
        int rowCount = CatalogTbl.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)CatalogTbl.getModel();
        for(int i = rowCount-1 ; i>=0 ; i--){
            dtm.removeRow(i);
        }       
        for(Supplier supplier : supplierDirectory.getSupplierDirectory())
        {   
            Object row[] = new Object[1];
            row[0] = supplier;
            dtm.addRow(row);
           }
    }
    
