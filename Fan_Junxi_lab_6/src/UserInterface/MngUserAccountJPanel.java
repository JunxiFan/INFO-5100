    private void removeBtunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtunActionPerformed
        // TODO add your handling code here:
        int selectRow = userAccountTbl.getSelectedRow();
        if (selectRow >= 0) {
            UserAccount ua = (UserAccount) userAccountTbl.getValueAt(selectRow, 0);
            if (ua.getRle().equals(UserAccount.ADMIN_ROLE)) {
                JOptionPane.showMessageDialog(this, "Admin account can't be deleted", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            business.getUserAccountDirectory().deleteUserAccount(ua);
            JOptionPane.showMessageDialog(this, "Account deleted.");

        } else {
            JOptionPane.showMessageDialog(this, "Please select any row.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_removeBtunActionPerformed

