    public void refreshTbl() {
        DefaultTableModel dtm = (DefaultTableModel) userAccountTbl.getModel();
        dtm.setRowCount(0);
        for (UserAccount ua : business.getUserAccountDirectory().getUserAccountList()) {
            Object row[] = new Object[3];
            row[0] = ua;
            row[1] = ua.getUserName();
            row[2] = ua.getRle();
            dtm.addRow(row);
        }
    }

